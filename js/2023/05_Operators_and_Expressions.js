// JavaScript Assignment
// The Assignment Operator (=) assigns a value to a variable:

var x = 10;
var y = 20;
console.log(x);
console.log(y);




// JavaScript Addition
// The Addition Operator (+) adds numbers:

// Adding
var x = 5;
var y = 2;
var z = x + y;
console.log(z);


// JavaScript Multiplication
// The Multiplication Operator (*) multiplies numbers:

// Multiplying
var x = 5;
var y = 2;
var z = x * y;
console.log(z);


